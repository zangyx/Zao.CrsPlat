        /// <summary>
        /// 将byte[]转换成int
        /// </summary>
        /// <param name="data">需要转换成整数的byte数组</param>
        public static int BytesToInt32(byte[] data)
        {
            if (data.Length < 4)
            {
                return 0;
            }


            var num = 0;


            if (data.Length >= 4)
            {
                var tempBuffer = new byte[4];


                Buffer.BlockCopy(data, 0, tempBuffer, 0, 4);


                num = BitConverter.ToInt32(tempBuffer, 0);
            }


            return num;
        }




